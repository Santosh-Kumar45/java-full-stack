import java.util.HashSet;

public class HashSet_demo {
    public static void main(String[] args) {
        HashSet<Integer> hashset=new HashSet<>();
        hashset.add(10);
        hashset.add(40);
        hashset.add(20);
        hashset.add(30);
        hashset.add(60);
        
        System.out.println(hashset);
    }
}
